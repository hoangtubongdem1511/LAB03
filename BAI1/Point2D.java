public class Point2D {
    public float x;
    public float y;
    
    public Point2D() {
        this.x = 0;
        this.y = 0;
    }
    
    public Point2D(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public float getX() {
        return x;
    }
    
    public float getY() {
        return y;
    }
}

